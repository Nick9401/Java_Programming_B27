package day06_operators;

public class CharIncrement {
    public static void main(String[] args) {
        char letter = 'A';
        System.out.println(letter++);
        System.out.println(letter++);
        System.out.println(letter++);
        System.out.println(letter++);

        char letter2 = 'a';
        System.out.println(++letter2);
        System.out.println(++letter2);
        System.out.println(++letter2);
        System.out.println(++letter2);

        char digits = '9';
        System.out.println(digits--);
        System.out.println(digits--);
        System.out.println(digits--);
        System.out.println(digits--);

        char number ='9';
        System.out.println(--number);
        System.out.println(--number);
        System.out.println(--number);
        System.out.println(--number);


    }



}
