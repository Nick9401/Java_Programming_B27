package day20_Loops;

public class th {
    public static void main(String[] args) {

        for (char letter = 'A'; letter <= 'Z'; letter++) {
            System.out.print(letter + "");


        }
        System.out.println();
        for (char letter1 = 'z'; letter1 >= 'a'; letter1--) {
            System.out.print(letter1 + "");



        }
    }
}